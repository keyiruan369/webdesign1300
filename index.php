            <p>Over 100 vendors will be selling unique apple varieties, hot apple cider
            donuts, kettle corn, local farm fresh baked goods and a multitude of around
            the world specialties. Local farmers are on site selling everything from tasty
            apple beverages to local pumpkins and of course, apples, apples and more 
            apples. Wineries and cideries have regional beverages available for tasting 
            and purchase.  Kids can visit with the goats and learn how fiber is made with 
            Laughing Goat Fiber Farm.  Looking for quality goods, visit 60 different
            handcrafting artisans along Cayuga Street at the fall craft show which includes
            jewelry, ceramics, home decor, body care, specialty gifts, unique sculpture, clothing and 
            so much more. </p>
